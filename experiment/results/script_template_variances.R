# selecting the current directory
setwd("<directory>")

# read csv file
data <- read.csv("<output_file_name>")

cat("\n\n\n")
print("ANOVA - F-MEASURE")
aov_results = aov(fmeasure ~ method, data=data)
print(summary(aov_results))
cat("\n\n")
print("Pairwise t-Student:")
pairwise_results = pairwise.t.test(data$fmeasure, data$method, p.adjust="hochberg")
print(pairwise_results)
cat("\n\n")
print("Tukey's Honest Significance Test:")
tukey_results = TukeyHSD(aov_results, conf.level = 0.95)
print(tukey_results)

