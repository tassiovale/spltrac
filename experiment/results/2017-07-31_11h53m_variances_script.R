# read csv file
data <- read.csv("2017-07-31_11h53m_output.csv")

cat("\n\n\n")
cat("F-MEASURE TESTS\n\n")
cat("Applying the Shapiro-Wilk normality test\n")
normality = shapiro.test(data$fmeasure)
print(normality)
if(normality$p.value < 0.05){
    cat("The sample has a normal distribution!")
    cat("\n\n")
    cat("1) ANOVA:\n")
    aov_results = aov(fmeasure ~ method, data=data)
    print(summary(aov_results))
    cat("\n\n")
    cat("2) Pairwise t-Student:\n")
    pairwise_results = pairwise.t.test(data$fmeasure, data$method, p.adjust="hochberg")
    print(pairwise_results)
    cat("\n\n")
    cat("3) Tukey's Honest Significance Test:\n")
    tukey_results = TukeyHSD(aov_results, conf.level = 0.95)
    print(tukey_results)
} else {
    cat("The sample has NOT a normal distribution!")
    cat("\n\n")
    cat("1) Kruskal Wallis test one-way ANOVA by ranks:\n")
    kruskal_results = kruskal.test(data$fmeasure~data$method)
    print(kruskal_results)
}

